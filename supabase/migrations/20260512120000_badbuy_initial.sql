CREATE TABLE IF NOT EXISTS currency_rate (
  id          uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  base        text        NOT NULL DEFAULT 'USD',
  target      text        NOT NULL,
  rate        decimal     NOT NULL,
  fetched_at  timestamptz NOT NULL DEFAULT now(),

  UNIQUE (base, target)
);

-- -----------------------------------------------------------------------------
-- 2.8 Referral Redemptions
-- -----------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS referral_redemption (
  id                      uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  redeemer_account_id     uuid        NOT NULL REFERENCES account(id),
  referrer_account_id     uuid        REFERENCES account(id),          -- NULL if seed code used
  promo_code_id           uuid        REFERENCES promo_code(id),       -- NULL if user referral used
  premium_months_granted  integer     NOT NULL DEFAULT 3,
  redeemed_at             timestamptz NOT NULL DEFAULT now(),

  -- One redemption per account, ever
  UNIQUE (redeemer_account_id),

  -- Exactly one of the two code columns must be populated
  CONSTRAINT chk_referral_xor_promo
    CHECK (num_nonnulls(referrer_account_id, promo_code_id) = 1),

  -- No self-referral
  CONSTRAINT chk_no_self_referral
    CHECK (redeemer_account_id != referrer_account_id)
);

-- -----------------------------------------------------------------------------
-- 2.9 Notifications
-- -----------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS notification (
  id              uuid               PRIMARY KEY DEFAULT gen_random_uuid(),
  account_id      uuid               NOT NULL REFERENCES account(id) ON DELETE CASCADE,
  type            notification_type  NOT NULL,
  tracked_item_id uuid               NOT NULL REFERENCES tracked_item(id) ON DELETE CASCADE,
  created_at      timestamptz        NOT NULL DEFAULT now(),
  read_at         timestamptz                                           -- NULL = unread
);


