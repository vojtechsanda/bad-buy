CREATE TABLE IF NOT EXISTS account (
  id                      uuid        PRIMARY KEY,          -- = auth.uid()
  name                    text,                             -- null until onboarding is complete
  avatar_seed             text,                             -- defaults to id via trigger
  birthdate               date,
  country                 text,                             -- ISO 3166-1 alpha-2
