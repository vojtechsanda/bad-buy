-- Trigger: keep account.decision_count in sync when status changes to bought/skipped
CREATE OR REPLACE FUNCTION trg_tracked_item_decision_count()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  -- On INSERT with a terminal status
  IF TG_OP = 'INSERT' AND NEW.status IN ('bought', 'skipped') THEN
    UPDATE account SET decision_count = decision_count + 1 WHERE id = NEW.account_id;

  -- On UPDATE where status transitions INTO a terminal state
  ELSIF TG_OP = 'UPDATE'
    AND OLD.status = 'frozen'
    AND NEW.status IN ('bought', 'skipped') THEN
    UPDATE account SET decision_count = decision_count + 1 WHERE id = NEW.account_id;
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tracked_item_decision_count ON tracked_item;
CREATE TRIGGER tracked_item_decision_count
  AFTER INSERT OR UPDATE OF status ON tracked_item
  FOR EACH ROW EXECUTE FUNCTION trg_tracked_item_decision_count();

